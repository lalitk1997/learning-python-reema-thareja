#WAP applying arithmetic operation on floating-point numbers

num1 = float(input("Enter number-1 : "))
num2 = float(input("Enter number-2 : "))
#arithematic operation +, -, /, *, %, **, //
print("Addition : "+str(num1 + num2))
print("Subtraction : "+str(num1 - num2))
print("Multiplication : "+str(num1 * num2))
print("Remainder (Mod) : "+str(num1 % num2))
print("Exponent : "+str(num1 ** num2))
print("Quotient : "+str(num1 // num2))