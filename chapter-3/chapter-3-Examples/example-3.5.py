#WAP to perform addition, subtraction, multiplication, division, integer division, and modulo  on two integer number
num1 = int(input("Enter value num1 :"))
num2 = int(input("Enter value num2 :"))
print("Sum : "+str(num1 + num2))
print("Sub : "+str(num1 - num2))
print("Mul : "+str(num1 * num2))
print("Div : "+str(num1 / num2))
print("Int Div : "+str(int(num1 / num2)))
print("Mod : "+str(num1 % num2))