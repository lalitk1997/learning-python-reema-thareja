#WAP to perform addition, subtraction, division, and multiplication on two floating point numbers
num1 = float(input("Enter value num1 :"))
num2 = float(input("Enter value num2 :"))
print("Addition : "+str(float(num1 + num2)))
print("Subtraction : "+str(float(num1 - num2)))
print("Division : "+str(float(num1 / num2)))
print("Multiplication : "+str(float(num1 * num2)))