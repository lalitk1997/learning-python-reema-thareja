#WAP to demonstrates the use of relational operators
num1 = int(input("Enter value num1 :"))
num2 = int(input("Enter value num2 :"))
print("== "+str(num1 == num2))
print("<= "+str(num1 <= num2))
print(">= "+str(num1 >= num2))
print("< "+str(num1 < num2))
print("> "+str(num1 > num2))
print("!= "+str(num1 != num2))